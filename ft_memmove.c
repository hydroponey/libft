#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned int	i;
	unsigned char	*s;
	unsigned char	*d;

	if (dst == src)
		return (dst);
	s = (unsigned char*)src;
	d = (unsigned char*)dst;
	i = 0;
	if (src < dst)
		while (i < len)
		{
			d[len - 1] = s[len - 1];
			len--;
		}
	else
		while (i < len)
		{
			d[i] = s[i];
			i++;
		}
	return (dst);
}
